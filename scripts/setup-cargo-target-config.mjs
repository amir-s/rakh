import { access, mkdir, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
// The legacy filename lets local overrides stay untracked on stable Cargo
// without depending on newer config include support.
const localConfigPath = path.join(repoRoot, "src-tauri", ".cargo", "config");

function resolveCacheRoot() {
  if (process.env.RAKH_CARGO_TARGET_DIR) {
    return process.env.RAKH_CARGO_TARGET_DIR;
  }

  const home = os.homedir();

  if (process.platform === "win32") {
    return path.join(
      process.env.LOCALAPPDATA || process.env.APPDATA || path.join(home, "AppData", "Local"),
      "rakh",
      "cargo-target",
    );
  }

  if (process.platform === "darwin") {
    return path.join(home, "Library", "Caches", "rakh", "cargo-target");
  }

  return path.join(process.env.XDG_CACHE_HOME || path.join(home, ".cache"), "rakh", "cargo-target");
}

function cargoPath(value) {
  return value.replace(/\\/g, "/");
}

async function main() {
  try {
    await access(localConfigPath);
    console.log(`cargo local config already exists at ${localConfigPath}`);
    return;
  } catch {
    // File does not exist yet; create it.
  }

  const targetDir = resolveCacheRoot();
  await mkdir(path.dirname(localConfigPath), { recursive: true });
  await mkdir(targetDir, { recursive: true });

  const contents = [
    "# Auto-generated by npm prepare. Keep this file untracked.",
    "[build]",
    `target-dir = "${cargoPath(targetDir)}"`,
    "",
  ].join("\n");

  await writeFile(localConfigPath, contents, "utf8");
  console.log(`created cargo local config at ${localConfigPath}`);
  console.log(`shared cargo target dir: ${targetDir}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
